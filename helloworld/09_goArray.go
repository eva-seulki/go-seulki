// 3. 다차원 배열의 초기화
// 2개의 행과 3개의 열을 이루는 배열의 초기화 예제
func main() {
	var a = [2][3]int{
		{1, 2, 3},
		{4, 5, 6}, // 끝에 콤마 추가 <======= 요거 주의하자!!!!!!!
	}
	println(a[1][2]) // 인덱스 주의해, 멍때리다 틀리는 수가 있음!!
}
