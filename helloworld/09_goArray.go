// 1. 배열 (Array)
// 배열 : 연속적 메모리 공간에 동일한 타입의 데이터를 순서적으로 저장하는 자료구조 (인덱스는 자바와 동일하게 Zero based array)
// 배열 선언 : var 변수명 [배열크기] 데이터 타입
/**
** 특징 : 배열크기도 Type을 구성하는 요소임. 즉, [3]int와 [5]int는 서로 다른 타입으로 인식됨
**/
package main

func main2() {
	var a [3]int //  정수형 3개 요소를 갖는 배열 a 선언
	a[0] = 1
	a[1] = 2
	a[2] = 3
	println(a[1]) //  2 출력
}

